package org.accela.file;

public class FDataFormatException extends RuntimeException
{
	private static final long serialVersionUID = 1L;

	public FDataFormatException()
	{
		super();
	}

	public FDataFormatException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public FDataFormatException(String message)
	{
		super(message);
	}

	public FDataFormatException(Throwable cause)
	{
		super(cause);
	}

}
